    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        
        if ( this.jTextFieldNombreProducto.getText().contentEquals("")||
               (this.jTextFieldPrecio.getText().contentEquals(""))||
                (this.jTextFieldExistencia.getText().contentEquals(""))){
            JOptionPane.showMessageDialog(null, "Uno de los campos requeridos esta vacio","Por favor revice los campos",
                                        JOptionPane.WARNING_MESSAGE);
        }else {
        String NomPr =this.jTextFieldNombreProducto.getText();
        double Pre = Double.parseDouble(this.jTextFieldPrecio.getText());
        int Exis = Integer.parseInt(jTextFieldExistencia.getText());
        
        Producto PRD = new Producto(NomPr, Pre, Exis);
        this.ListaProductos.add(PRD);
        }
        
    }//GEN-LAST:event_jButtonGuardarActionPerformed
