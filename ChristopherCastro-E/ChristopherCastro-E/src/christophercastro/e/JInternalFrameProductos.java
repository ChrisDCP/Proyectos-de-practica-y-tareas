    private void jButtonVerDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVerDatosActionPerformed
        String datos = "";
        
        for (Producto prd : ListaProductos) {
            datos = "Producto: "+prd.getNombreProducto()+"\n"+"Cantidad: "+prd.getCcantidad()+"\n"+"Precio :"+prd.getPrecio()+"\n"+"\n";
        }
        this.jTextAreaDatos.setText(datos);
    }//GEN-LAST:event_jButtonVerDatosActionPerformed


